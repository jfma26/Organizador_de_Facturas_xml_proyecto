    public void llenarCombo() throws SQLException {
        ConexionBase cont = new ConexionBase();
        String sentencia = "SELECT * FROM Limite_Gasto;";
        ResultSet resultado = cont.SelectDB1(sentencia);
        int codigo = 0;
        while (resultado.next()) {
            jComboBox1.addItem(resultado.getString("Año"));
            codigo++;
        }
    }

    private void LLenarCajasTexto() throws SQLException {
        ConexionBase cont = new ConexionBase();
        String sentencia = "SELECT * FROM Limite_Gasto where Año='" + jComboBox1.getSelectedItem().toString() + "';";
        ResultSet resultado = cont.SelectDB1(sentencia);
        int codigo = 0;
        model.setNumRows(3);
        int col = 1;
        model.setValueAt("Limite ", col, 0);
        while (resultado.next()) {
            // System.out.println(resultado.getString(Comprador));
            //model.setValueAt(resultado.getString("Valor_Total_Año"));
            model.setValueAt(resultado.getString("Valor_Vivienda"), col, 1);
            model.setValueAt(resultado.getString("Valor_Educacion"), col, 2);
            model.setValueAt(resultado.getString("Valor_Salud"), col, 3);
            model.setValueAt(resultado.getString("Valor_Vestido"), col, 4);
            model.setValueAt(resultado.getString("Valor_Alimentacion"), col, 5);
            model.setValueAt(resultado.getString("Valor_Otro"), col, 6);
            jTextField1.setText(resultado.getString("Valor_Vivienda"));
            jTextField2.setText(resultado.getString("Valor_Educacion"));
            jTextField3.setText(resultado.getString("Valor_Salud"));
            jTextField4.setText(resultado.getString("Valor_Vestido"));
            jTextField5.setText(resultado.getString("Valor_Alimentacion"));
            jTextField7.setText(resultado.getString("Valor_Otro"));
            jTextField8.setText(resultado.getString("Valor_Total_Año"));
            codigo++;
        }

    }

